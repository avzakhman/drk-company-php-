                        </p>
                        <p class="p-16-template">
                            Am häufigsten sehen wir bei NAS-Speichern Probleme, wenn mehr als eine Festplatte in einem RAID 5 oder RAID 6 Array ausgefallen ist. Dies kann bedeuten, dass mehrere Festplatten gleichzeitig einen Defekt aufweisen und nach Austausch der defekten Festplatte beim RAID-Wiederaufbau eine weitere Festplatte ausfällt. Dies hat dann den Crash des ganzen RAID-Array im NAS zur Folge.
                        </p>
